#!/usr/bin/env python
"""LLM-as-judge for the Headroom accuracy eval.

Grades a model's answer against explicit ground truth. Design choices that keep
the verdict trustworthy:
  - judged by a STRONG model (Sonnet) different from the generator (avoids
    self-preference bias when Headroom answers are generated by Haiku);
  - temperature 0 for determinism;
  - graded INDEPENDENTLY against ground truth (no A/B in one prompt -> no
    position bias toward "baseline" or "headroom");
  - structured JSON verdict {correct, score, reasoning};
  - optional self-consistency via N votes (majority on `correct`).

The judge never sees which path (baseline / headroom) produced the answer.
"""

from __future__ import annotations

import json
import re
from typing import Any

JUDGE_MODEL = "claude-sonnet-4-6"

_RUBRIC = """\
You are a strict grader. Decide whether the ANSWER correctly responds to the \
QUESTION, using the GROUND TRUTH as the authority for what is correct.

Rules:
- The answer is CORRECT only if it states every required fact from the ground \
truth, with the exact values (ids, codes, numbers, names) matching. Paraphrasing \
of wording is fine; the specific values must be right.
- Extra commentary is fine as long as the required values are present and not \
contradicted.
- If a required value is missing, wrong, or hedged into ambiguity, it is INCORRECT.
- Ignore formatting, ordering, and politeness. Judge substance only.

Return ONLY a JSON object on a single line, no prose, no code fence:
{"correct": true|false, "score": 0.0-1.0, "reasoning": "<one sentence>"}
score = your confidence the answer is fully correct (1.0 = certainly correct).
"""


def _extract_json(text: str) -> dict[str, Any]:
    # Prefer a fenced or bare JSON object; fall back to first {...} span.
    m = re.search(r"\{.*\}", text, re.DOTALL)
    if not m:
        return {"correct": False, "score": 0.0, "reasoning": f"unparseable judge output: {text[:120]}"}
    try:
        obj = json.loads(m.group(0))
    except Exception:
        return {"correct": False, "score": 0.0, "reasoning": f"bad json: {text[:120]}"}
    obj.setdefault("correct", False)
    obj.setdefault("score", 1.0 if obj["correct"] else 0.0)
    obj.setdefault("reasoning", "")
    obj["correct"] = bool(obj["correct"])
    try:
        obj["score"] = float(obj["score"])
    except Exception:
        obj["score"] = 1.0 if obj["correct"] else 0.0
    return obj


def judge_answer(
    client: Any,
    question: str,
    reference_answer: str,
    answer: str,
    model: str = JUDGE_MODEL,
    votes: int = 1,
) -> dict[str, Any]:
    """Return {correct, score, reasoning, votes:[...]}. Majority vote if votes>1."""
    if not answer or not answer.strip():
        return {"correct": False, "score": 0.0, "reasoning": "empty answer", "votes": []}

    user = (
        f"QUESTION:\n{question}\n\n"
        f"GROUND TRUTH:\n{reference_answer}\n\n"
        f"ANSWER TO GRADE:\n{answer}\n"
    )
    verdicts: list[dict[str, Any]] = []
    for _ in range(max(1, votes)):
        resp = client.messages.create(
            model=model,
            system=_RUBRIC,
            messages=[{"role": "user", "content": user}],
            max_tokens=200,
            temperature=0,
        )
        text = "".join(b.text for b in resp.content if getattr(b, "type", "") == "text")
        verdicts.append(_extract_json(text))

    n_correct = sum(1 for v in verdicts if v["correct"])
    correct = n_correct * 2 > len(verdicts)  # strict majority
    score = sum(v["score"] for v in verdicts) / len(verdicts)
    reasoning = verdicts[0]["reasoning"]
    return {"correct": correct, "score": round(score, 3), "reasoning": reasoning, "votes": verdicts}


def needle_match(answer: str, needles: list[str]) -> bool:
    """Cheap secondary signal: do all ground-truth needles appear verbatim?"""
    low = (answer or "").lower()
    return all(n.lower() in low for n in needles)
